    public static void GetFilter(JTable table,String text){
        TableRowSorter sorter = new TableRowSorter(table.getModel());
        if(text.trim().length()==0){
            sorter.setRowFilter(null);
        }
        else{
            sorter.setRowFilter(RowFilter.regexFilter("(?i)"+text));
        }
        table.setRowSorter(sorter);
    }
    
    private void InitTableKho(){
        KhoBUS dskho = new KhoBUS();
        try {
            dskho.TaiDSKho();
        } catch (Exception ex) {
            Logger.getLogger(KhoFRM.class.getName()).log(Level.SEVERE, null, ex);
        }
        Vector header = new Vector();
        header.add("Ma Kho");
        header.add("Ten Kho");
        header.add("Dia chi");
        header.add("So dien thoai");
        header.add("Noi dung");
        DefaultTableModel model = new DefaultTableModel(header,0){
            @Override
            public boolean isCellEditable(int row,int column){
                return false;
            }
            @Override
            public Class getColumnClass(int column){
                switch (column){
                    case 0:
                        return String.class;
                    case 1:
                        return String.class;
                    case 2:
                        return String.class;
                    case 3:
                        return String.class;
                    case 4:
                        return String.class;
                    default:
                        return String.class;
                }
            }
        };
        for (KhoDTO kho : KhoBUS.dskho) {
            Vector row = new Vector();
            row.add(kho.getMakho());
            row.add(kho.getTenkho());
            row.add(kho.getDiachi());
            row.add(kho.getSodienthoai());
            row.add(kho.getNoidung());
            model.addRow(row);
        }
        tblKho.setModel(model);
    }
    
